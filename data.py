def calculate_score(yongshen_wx: str, month_wx: str, day_wx: str,
                    month_dz: str = "", day_dz: str = "", action_info: list = None) -> dict:
    """
    计算用神综合得分。
    get_wuxing_relation(用神, 月建/日辰) 返回的是从月建/日辰视角看用神的关系。
    
    关系对照（ref=月建/日辰）：
      "我生" = ref生target = 月建/日辰生用神 → +2分
      "生我" = target生ref = 用神生月建/日辰 → 休囚 -1分
      "我克" = ref克target = 月建/日辰克用神 → -2分
      "克我" = target克ref = 用神克月建/日辰 → 休囚 -1分
      "同"  = 同五行 → 旺相 +3分
    """
    result = {
        "用神五行": yongshen_wx,
        "月建五行": month_wx,
        "日辰五行": day_wx,
    }

    # 月建生克判定（月建主动作用用神，用神不能反作用日月）
    rel_m = get_wuxing_relation(yongshen_wx, month_wx)
    if rel_m == "同":       # 同五行（当令旺相）
        result["月建评分"] = MONTH_SCORE["旺"]
        result["月建关系"] = "月建与用神同五行（旺相）"
    elif rel_m == "我生":   # 月建生用神（相）
        result["月建评分"] = MONTH_SCORE["相"]
        result["月建关系"] = "月建生用神（相）"
    elif rel_m == "我克":   # 月建克用神（死）
        result["月建评分"] = MONTH_SCORE["死"]
        result["月建关系"] = "月建克用神（死）"
    else:                   # 用神生月建、用神克月建等 → 用神不能反作用月建，按无关计
        result["月建评分"] = MONTH_SCORE["平"]
        result["月建关系"] = "无关"

    # 日辰生克判定（日辰主动作用用神，用神不能反作用日辰）
    rel_d = get_wuxing_relation(yongshen_wx, day_wx)
    if rel_d == "同":
        result["日辰评分"] = DAY_SCORE["旺"]
        result["日辰关系"] = "日辰与用神同"
    elif rel_d == "我生":   # 日辰生用神
        result["日辰评分"] = DAY_SCORE["生"]
        result["日辰关系"] = "日辰生用神"
    elif rel_d == "我克":   # 日辰克用神
        result["日辰评分"] = DAY_SCORE["克"]
        result["日辰关系"] = "日辰克用神"
    else:                   # 用神生日辰、用神克日辰等 → 用神不能反作用日辰，按无关计
        result["日辰评分"] = DAY_SCORE["平"]
        result["日辰关系"] = "无关"

    # 月建/日辰与用神所在爻的冲害（简化为地支对冲同五行判定）
    # 实际应查用神所在爻的地支，目前简化处理
    # （预留接口，后续可精确到爻位地支）
    
    # 动变生克中的刑冲害在 get_action_info 中处理

    # 动变生克
    action_score = 0
    action_detail = []
    if action_info:
        for action in action_info:
            action_score += action["score"]
            action_detail.append(action["desc"])

    result["动变评分"] = action_score
    result["动变详情"] = "；".join(action_detail) if action_detail else "无动爻"

    # 综合评分
    total = result["月建评分"] + result["日辰评分"] + action_score
    result["综合评分"] = total

    # 吉凶判定（新阈值）
    if total >= 6:
        result["吉凶"] = "大吉"
    elif total >= 3:
        result["吉凶"] = "吉"
    elif total >= -1:
        result["吉凶"] = "平"
    elif total >= -4:
        result["吉凶"] = "凶"
    else:
        result["吉凶"] = "大凶"

    return result
