def get_shi_yao(shang_gua: str, xia_gua: str) -> tuple:
    """
    获取世爻信息
    returns: (世爻位置, 世爻地支, 世爻五行, 所属宫, 宫首卦五行)
    
    注意：六亲是以宫首卦的五行来定的，不是世爻自身的纳甲五行。
    例如坎宫首卦坎为水，则父母=金、官鬼=土、妻财=火、兄弟=水、子孙=木。
    世爻的纳甲地支五行用于六兽和生克判定，不用于六亲。
    """
    key = shang_gua + xia_gua
    info = SHI_GONG.get(key)
    if not info:
        return (6, "子", "水", "乾", "金")  # fallback
    
    gong = info["宫"]
    shi_pos = info["世"]
    
    # 宫首卦五行（用于定六亲）
    gong_wx = BAGUA_WUXING.get(gong, "水")
    
    # 世爻自身的纳甲地支五行（用于六兽、生克等）
    pos_map = {1: "初", 2: "二", 3: "三", 4: "四", 5: "五", 6: "六"}
    pos_key = pos_map[shi_pos]
    
    # 世爻的纳甲：看世爻属于上卦还是下卦
    # 爻位1-3在下卦，4-6在上卦
    if shi_pos <= 3:
        shi_gua = xia_gua
    else:
        shi_gua = shang_gua
    
    if shi_gua in NAJIA and pos_key in NAJIA[shi_gua]:
        dz, wx = NAJIA[shi_gua][pos_key]
    else:
        dz, wx = "子", "水"
    
    return (shi_pos, dz, wx, gong, gong_wx)

