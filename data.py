def get_liuqin_by_gong(gong_wx: str, line_wx: str) -> str:
    """
    根据宫首卦五行和爻位五行，返回该爻对应的六亲
    """
    rel = get_wuxing_relation(line_wx, gong_wx)
    # rel是从宫首卦(me)视角看爻的五行
    # 注意这里的参数：target=爻五行, ref=宫首卦五行
    # 例如：宫首卦=水，爻=金 → get_wuxing_relation("金","水")
    # wuxing_sheng("水")="木"≠"金" | wuxing_bei_sheng("水")="金"=="金" → "生我"
    # "生我" = 生宫首卦 = 父母
    
    mapping = {
        "生我": "父母",  # 爻生宫首卦
        "我生": "子孙",  # 宫首卦生爻
        "克我": "官鬼",  # 爻克宫首卦
        "我克": "妻财",  # 宫首卦克爻
        "同": "兄弟",    # 同五行
    }
    return mapping.get(rel, "兄弟")

# === 通用农历月建（基于节气近似，适配任何年份） ===
# 节气日期：立春2/4 惊蛰3/6 清明4/5 立夏5/6 芒种6/6
#             小暑7/7 立秋8/7 白露9/8 寒露10/8 立冬11/7 大雪12/7 小寒1/6
JIEQI = [
    (1, 6, "丑"), (2, 4, "寅"), (3, 6, "卯"), (4, 5, "辰"),
    (5, 6, "巳"), (6, 6, "午"), (7, 7, "未"), (8, 7, "申"),
    (9, 8, "酉"), (10, 8, "戌"), (11, 7, "亥"), (12, 7, "子"),
]

def get_jieqi_month(dt=None):
    """通过节气确定月建地支"""
    from datetime import datetime
    if dt is None:
        dt = datetime.now()
    m, d = dt.month, dt.day
    for sm, sd, dz in JIEQI:
        if m < sm or (m == sm and d < sd):
            # 取上一个节气
            idx = JIEQI.index((sm, sd, dz)) - 1
            return JIEQI[idx][2]
    return JIEQI[-1][2]  # 大雪之后到年底

def get_lunar_month_info(dt=None):
    dz = get_jieqi_month(dt)
    wx = DIZHI_WUXING.get(dz, "木")
    return dz, wx

def get_ganzhi_from_date(dt=None):
    """
    获取日干支（简化版：从已知基准日推算）
    returns: (天干, 地支)
    基准：2024-01-01 = 甲子日
    """
    from datetime import datetime
    if dt is None:
        dt = datetime.now()
    base = datetime(2024, 1, 1)
    delta = (dt - base).days
    tian_gan = ["甲", "乙", "丙", "丁", "戊", "己", "庚", "辛", "壬", "癸"]
    di_zhi = ["子", "丑", "寅", "卯", "辰", "巳", "午", "未", "申", "酉", "戌", "亥"]
    gan = tian_gan[delta % 10]
    zhi = di_zhi[delta % 12]
    return gan, zhi

def get_liuyao_full(shang_gua: str, xia_gua: str, gong: str, gong_wx: str,
                    shi_pos: int, ying_pos: int, dong_yao: int = 0,
                    gan: str = "甲") -> list:
    """
    生成完整的六爻排盘
    纳甲使用上下卦各自的纳甲（初-三爻用下卦，四-六爻用上卦）
    """
    pos_map = {1: "初", 2: "二", 3: "三", 4: "四", 5: "五", 6: "六"}
    liu_shen = get_liushen_by_day(gan)
    
    lines = []
    for i in range(1, 7):  # 初爻=1, 六爻=6
        pos_key = pos_map[i]
        
        # 纳甲：初-三爻用下卦，四-六爻用上卦
        gua = xia_gua if i <= 3 else shang_gua
        if gua in NAJIA and pos_key in NAJIA[gua]:
            dz, wx = NAJIA[gua][pos_key]
        else:
            dz, wx = "子", "水"
        
        # 六亲
        liu_qin = get_liuqin_by_gong(gong_wx, wx)
        
        # 世应
        shi_ying = ""
        if i == shi_pos:
            shi_ying = "世"
        if i == ying_pos:
            shi_ying = "应"
        
        # 动爻标记
        is_dong = (i == dong_yao)
        
        # 六兽（从初爻起）
        shen = liu_shen[i - 1]
        
        line = {
            "爻位": pos_key,
            "六兽": shen,
            "六亲": liu_qin,
            "地支": dz,
            "五行": wx,
            "世应": shi_ying,
            "动爻": is_dong,
        }
        lines.append(line)
    
    return lines

